.global asm_reverse
asm_reverse:
    mov x8, x0          
    
find_end_neon:
    ld1 {v0.16b}, [x8], #16
    uminv b1, v0.16b    
    fmov w9, s1
    cbz w9, found_null_neon  
    b find_end_neon

found_null_neon:
    sub x10, x8, x0      // Длина (примерная)
    add x2, x0, x10
    sub x2, x2, #32      // Указатель на правый блок (32 байта от конца)
    mov x1, x0           // Указатель на левый блок

neon_loop_32:
    cmp x1, x2
    b.ge simple_tail

    // 1. Загружаем 32 байта слева (v0, v1) и 32 байта справа (v2, v3)
    ld1 {v0.16b, v1.16b}, [x1]
    ld1 {v2.16b, v3.16b}, [x2]

    // 2. Реверс байтов внутри каждого регистра
    rev64 v0.16b, v0.16b
    ext v0.16b, v0.16b, v0.16b, #8
    rev64 v1.16b, v1.16b
    ext v1.16b, v1.16b, v1.16b, #8
    rev64 v2.16b, v2.16b
    ext v2.16b, v2.16b, v2.16b, #8
    rev64 v3.16b, v3.16b
    ext v3.16b, v3.16b, v3.16b, #8

    // 3. ЗАПИСЬ (Магия здесь):
    // Блок, который был в самом конце (v3), должен пойти в самое начало.
    // Блок, который был вторым с конца (v2), идет следом.
    st1 {v3.16b}, [x1], #16
    st1 {v2.16b}, [x1], #16
    
    // Блок, который был вторым с начала (v1), идет на место v2.
    // Блок, который был самым первым (v0), идет на место v3.
    st1 {v1.16b}, [x2], #16
    st1 {v0.16b}, [x2]
    
    // Возвращаем x2 назад на 48 байт (прошли 16 вперед, нужно откатиться на 32 назад от старта)
    sub x2, x2, #48
    
    b neon_loop_32

simple_tail:
    ret

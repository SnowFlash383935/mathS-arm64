.global asm_reverse
asm_reverse:
    // x0 - адрес строки
    mov x8, x0          // Сохраняем начало
    
    // Быстрый поиск конца строки (аналог strlen, но свой)
find_end:
    ld1 {v0.16b}, [x8], #16
    uminv b1, v0.16b    // Находим минимальный байт в векторе
    fmov w9, s1
    cbz w9, found_null  // Если есть 0, значит конец близко
    b find_end

found_null:
    sub x1, x8, x0      // x1 = примерная длина
    add x2, x0, x1
    sub x2, x2, #32     // x2 = указатель на конец (минус 32 байта)
    mov x1, x0          // x1 = указатель на начало

neon_loop_32:
    cmp x1, x2
    b.ge tail_loop      // Если указатели встретились, доделываем остаток

    // Загружаем по 32 байта с каждой стороны
    ld1 {v0.16b, v1.16b}, [x1]
    ld1 {v2.16b, v3.16b}, [x2]

    // Реверс байт в каждом 16-байтовом регистре
    rev64 v0.16b, v0.16b
    ext v0.16b, v0.16b, v0.16b, #8
    rev64 v1.16b, v1.16b
    ext v1.16b, v1.16b, v1.16b, #8
    rev64 v2.16b, v2.16b
    ext v2.16b, v2.16b, v2.16b, #8
    rev64 v3.16b, v3.16b
    ext v3.16b, v3.16b, v3.16b, #8

    // Меняем блоки местами и записываем
    // (v0, v1) уходят на место (v2, v3) в обратном порядке
    st1 {v3.16b, v2.16b}, [x1], #32
    st1 {v1.16b, v0.16b}, [x2]
    sub x2, x2, #32
    
    b neon_loop_32

tail_loop:
    // Здесь можно оставить старый ldrb/strb для последних штрихов
    ret

tail_loop:
    // Тут должен быть твой старый побайтовый код для остатка (хвоста), 
    // если длина не кратна 16.

rev_done:
    ldp x29, x30, [sp], #16
    ret

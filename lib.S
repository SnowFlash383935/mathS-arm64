.global asm_reverse
asm_reverse:
    // x0 - адрес строки
    mov x1, x0          // x1 - левый указатель
    
    // 1. Ищем реальный конец строки
    mov x8, x0
find_null:
    ldrb w9, [x8], #1
    cbnz w9, find_null
    sub x2, x8, #2      // x2 указывает на последний символ перед \0

neon_loop_32:
    // Проверяем, осталось ли еще хотя бы 32 байта между указателями
    sub x10, x2, x1
    cmp x10, #31
    b.lt tail_byte      // Если меньше 32 байт — идем в хвост

    // Загружаем 16 байт слева и 16 байт справа
    // (Для простоты возьмем по 16, это стабильнее)
    ld1 {v0.16b}, [x1]
    ld1 {v1.16b}, [x2], #-15 // Загружаем и сдвигаем x2 назад
    
    // Реверс внутри регистров
    rev64 v0.16b, v0.16b
    ext v0.16b, v0.16b, v0.16b, #8
    rev64 v1.16b, v1.16b
    ext v1.16b, v1.16b, v1.16b, #8

    // Записываем: левый развернутый (v0) уходит на место правого (v1)
    st1 {v0.16b}, [x2]
    // Правый развернутый (v1) уходит на место левого (v0)
    st1 {v1.16b}, [x1], #16
    
    // Указатель x2 уже сдвинут на -15 (после st1 нужно еще на -1, итого -16)
    sub x2, x2, #1
    b neon_loop_32

tail_byte:
    // Побайтовый реверс для остатка в центре
    cmp x1, x2
    b.ge rev_done
    ldrb w3, [x1]
    ldrb w4, [x2]
    strb w3, [x2], #-1
    strb w4, [x1], #1
    b tail_byte

rev_done:
    ret

.global asm_reverse
asm_reverse:
    // x0 - адрес начала строки
    // Нам нужна длина, для простоты возьмем её через strlen
    stp x29, x30, [sp, #-16]!
    mov x19, x0
    bl strlen               // Стандартный strlen из libc (он быстрый)
    mov x1, x0              // x1 = длина
    mov x0, x19             // x0 = начало
    
    cbz x1, rev_done        // Если длина 0, выходим
    
    add x2, x0, x1          // x2 указывает на конец (\0)
    sub x2, x2, #16         // Смещаемся назад на 16 байт для векторного чтения

neon_loop:
    cmp x0, x2
    b.ge tail_loop          // Если осталось меньше 16 байт, идем в хвост

    ld1 {v0.16b}, [x0]      // Загружаем 16 байт слева
    ld1 {v1.16b}, [x2]      // Загружаем 16 байт справа

    // Магия: разворачиваем порядок байт внутри 128-битного регистра
    // Инструкция tbl позволяет переставлять байты по маске, 
    // но для простоты используем реверс элементов
    rev64 v0.16b, v0.16b    // Реверс внутри 64-бит
    ext v0.16b, v0.16b, v0.16b, #8 // Меняем половинки местами

    rev64 v1.16b, v1.16b
    ext v1.16b, v1.16b, v1.16b, #8

    st1 {v1.16b}, [x0], #16 // Пишем правый блок налево и двигаем указатель
    st1 {v0.16b}, [x2]
    sub x2, x2, #16         // Двигаем правый указатель назад
    
    b neon_loop

tail_loop:
    // Тут должен быть твой старый побайтовый код для остатка (хвоста), 
    // если длина не кратна 16.

rev_done:
    ldp x29, x30, [sp], #16
    ret
